		public static double GetMeterPerDegree_Lon(double lat)
		{
			if (lat < 20.0 || 46.0 <= lat)
				throw new Exception("緯度が範囲外です。" + lat);

			lat *= 10.0;

			int iLat = (int)lat;
			double d = lat - iLat;

			iLat -= 200;

			return MPD_Lon_200To460[iLat] - (MPD_Lon_200To460[iLat] - MPD_Lon_200To460[iLat + 1]) * d;
		}

